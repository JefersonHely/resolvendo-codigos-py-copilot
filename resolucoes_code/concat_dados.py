# Vamos receber dois dados diferentes do usuário e concatena-los em uma única string.

# Receber dois dados do usuário
primeiro_dado = input("Digite o primeira informação: ")
segundo_dado = input("Digite o segunda informação: ")

# Concatenar os dados em uma única string
resultado = primeiro_dado + segundo_dado

# Exibir o resultado
print("A concatenação dos dados é:", resultado)